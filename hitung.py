a = 10
b = 3

hasil = a+b
print('Hasil dari penjumlahan tersebut adalah :',hasil)

hasil = a-b
print('Hasil dari pengurangan tersebut adalah :',hasil)

hasil = a*b
print('Hasil dari perkalian tersebut adalah :',hasil)

hasil = a/b
print('Hasil dari pembagian tersebut adalah :',hasil)

hasil = a**b
print('Hasil dari eksponen (pangkat) tersebut adalah :',hasil)

hasil = a%b
print('Hasil dari modulus tersebut adalah :',hasil)

hasil = a//b
print('Hasil dari floor division tersebut adalah :',hasil)